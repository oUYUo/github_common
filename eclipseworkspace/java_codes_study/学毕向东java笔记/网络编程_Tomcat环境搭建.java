/*
1，Tomcat官方网址：http://about.blank.la/
2，左侧 Download 类下的将是 Tomcat 版本，选一个。
3，根据系统下载免安装版，如：win7 64位系统 -- 64-bit Windows zip (pgp, md5) 
4，环境变量配置：在系统变量下新建
		变量名：JAVA_HOME		---->	值：jdk目录
		变量名：TOMCAT_HOME		---->	值：Tomcat目录
5，启动服务器：Tomcat\bin\startup.bat
6，关闭服务器：Tomcat\bin\shutdown.bat
7，Tomcat\webapps\myweb 目录中存放 html 文件。
8，访问 html 文件：
	1，开启服务器。
	2，打开浏览器。
	3，输入地址：127.0.0.1:8080/myweb/文件名.html
*/