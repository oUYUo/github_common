/*
变量定义：
1，变量将不确定的数据进行存储。
2，变量定义：数据类型 变量名。
3，定义float类型数据，需要在值后面加f，默认是double。 float fl = 3.2f
4，char类型定义：  ‘字符’  char cl = '4'
5，当数据不确定时，需要对数据进行型存储时，就定义变量来完成。
6，成员变量随着对象创建而创建。
7，静态变量随着类的加载而加载。
------------------------------------------------------------------------------
类型转换：
1，byte b = 3; b = b + 2; 编译错误，解释：2默认是int类型。强制转换：b = (byte)(b + 2);
2，字符和数字相加，结果是数字。 

------------------------------
注意，成员变量有默认值，不赋值可参与运算，而局部变量必须赋值。
*/