/*
流程控制：
if 如果
else 否则
else if  否则如果

switch  枚举
case 枚举接口
break 枚举结束、跳出循环体
default 默认接口

while  先判断条件，只有条件满足才执行循环体。循环判断首
do while 先执行循环体，然后判断条件，条件满足后，执行循环体。循环判断尾
for(变量初始化, 条件表达式, 递增)变量循环首
while与for区别：for的定义变量是局部变量，只能在for里面使用，而while是程序集变量


小细节：
1，在循环前添加标记，可在内循环用标记通过break结束标记的循环。
2，continue 继续循环：只能作用于循环结构，特点：结束本次循环，继续下一次循环。
*/