/*
对象：
1，创建对象： 对象类型 类类型变量 = new 对象名; ---> Car c = new Car();//类类型变量指向对象。
2，调用对象：  A，对象名.对象成员名;
			   B，对象名.方法名();
3，匿名对象：一次性调用，new 对象名();
	   作用：可以将匿名对象作为实际参数进行传递
	   比如：创建匿名对象，返回的地址，将地址传递方法，方法就可调用对象。
4，封装：将不需要对外提供的内容进行隐藏。
	A，public   --> 公开权限，最大的权限。
	B，private  --> 私有权限，只能在本类中访问。
	C，私有仅仅是封装的一种体现形式。

*/